/*
 * Render glyph bitmap ke buffer piksel ARGB.
 * Mengkonversi bitmap 1-bit ke buffer 32-bit ARGB.
 */
status_t font_bitmap_render(const glyph_bitmap_t *g,
                            tak_bertanda32_t warna,
                            glyph_terrender_t *hasil)
{
    tak_bertanda32_t jumlah_piksel;
    tak_bertanda8_t i, j;

    if (g == NULL || hasil == NULL) {
        return STATUS_PARAM_NULL;
    }
    if (g->data == NULL || g->lebar == 0 || g->tinggi == 0) {
        return STATUS_PARAM_INVALID;
    }

    kernel_memset(hasil, 0, sizeof(glyph_terrender_t));

    jumlah_piksel = (tak_bertanda32_t)g->lebar *
                    (tak_bertanda32_t)g->tinggi;
    hasil->piksel = (tak_bertanda32_t *)kmalloc(
        jumlah_piksel * sizeof(tak_bertanda32_t));
    if (hasil->piksel == NULL) {
        return STATUS_MEMORI_TIDAK_CUKUP;
    }

    hasil->lebar = g->lebar;
    hasil->tinggi = g->tinggi;
    hasil->x_bawa = g->x_ofset;
    hasil->y_bawa = g->y_ofset;
    hasil->x_lanjut = g->x_lanjut;
    hasil->y_lanjut = 0;

    for (j = 0; j < g->tinggi; j++) {
        for (i = 0; i < g->lebar; i++) {
            tak_bertanda32_t byte_idx;
            tak_bertanda8_t bit_idx;
            tak_bertanda8_t byte_val;
            tak_bertanda32_t idx;
            tak_bertanda32_t px;

            byte_idx = (tak_bertanda32_t)j *
                ((g->lebar + 7) / 8) + (i / 8);
            bit_idx = (tak_bertanda8_t)(7 - (i % 8));

            if (byte_idx >= g->ukuran) {
                idx = (tak_bertanda32_t)j *
                      g->lebar + i;
                hasil->piksel[idx] = 0;
                continue;
            }

            byte_val = g->data[byte_idx];
            idx = (tak_bertanda32_t)j * g->lebar + i;

            if ((byte_val & (1 << bit_idx)) != 0) {
                px = warna;
            } else {
                px = 0;
            }
            hasil->piksel[idx] = px;
        }
    }

    return STATUS_BERHASIL;
}
