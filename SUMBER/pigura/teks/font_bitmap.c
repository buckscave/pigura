/*
 * Buat font bitmap 8x8 terdegar dan daftarkan ke sistem.
 */
font_t *font_bitmap_buat_8x8(const char *nama)
{
    glyph_bitmap_t glyph_daftar[GLYPH_ASCII_JUMLAH];
    tak_bertanda16_t i;

    if (nama == NULL) {
        return NULL;
    }

    kernel_memset(glyph_daftar, 0,
        sizeof(glyph_daftar));

    for (i = 0; i < GLYPH_ASCII_JUMLAH; i++) {
        status_t st;
        st = glyph_buat_bitmap(&glyph_daftar[i],
            FONT_BITMAP_LEBAR_8, FONT_BITMAP_TINGGI_8,
            0, 0, FONT_BITMAP_LEBAR_8,
            font_8x8_data[i]);
        if (st != STATUS_BERHASIL) {
            tak_bertanda16_t j;
            for (j = 0; j < i; j++) {
                glyph_hapus_bitmap(
                    &glyph_daftar[j]);
            }
            return NULL;
        }
    }

    return font_daftar_bitmap(nama, glyph_daftar,
        GLYPH_ASCII_JUMLAH, FONT_BITMAP_TINGGI_8);
}

