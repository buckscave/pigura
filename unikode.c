/* Konversi UTF-8 ke codepoint Unicode */
int unikode_utf8_ke_codepoint(const char *str, unsigned int *codepoint) {
    unsigned char c;
    
    if (!str || !codepoint) {
        return 0;
    }
    
    c = (unsigned char)*str;
    
    /* ASCII (1 byte) */
    if (c < 0x80) {
        *codepoint = c;
        return 1;
    }
    
    /* 2 byte sequence */
    if ((c & 0xE0) == 0xC0) {
        if ((str[1] & 0xC0) != 0x80) {
            return 0; /* Sequence tidak valid */
        }
        *codepoint = ((c & 0x1F) << 6) | (str[1] & 0x3F);
        return 2;
    }
    
    /* 3 byte sequence */
    if ((c & 0xF0) == 0xE0) {
        if ((str[1] & 0xC0) != 0x80 || (str[2] & 0xC0) != 0x80) {
            return 0; /* Sequence tidak valid */
        }
        *codepoint = ((c & 0x0F) << 12) | 
                     ((str[1] & 0x3F) << 6) | 
                     (str[2] & 0x3F);
        return 3;
    }
    
    /* 4 byte sequence */
    if ((c & 0xF8) == 0xF0) {
        if ((str[1] & 0xC0) != 0x80 || 
            (str[2] & 0xC0) != 0x80 || 
            (str[3] & 0xC0) != 0x80) {
            return 0; /* Sequence tidak valid */
        }
        *codepoint = ((c & 0x07) << 18) | 
                     ((str[1] & 0x3F) << 12) | 
                     ((str[2] & 0x3F) << 6) | 
                     (str[3] & 0x3F);
        return 4;
    }
    
    return 0; /* UTF-8 tidak valid */
}

/* Dapatkan data font dari codepoint */
const unsigned char* unikode_data_font(unsigned int codepoint) {
    unsigned int i;
    
    /* Cari dalam tabel font Unicode */
    for (i = 0; i < JUMLAH_ENTRI_FONT; i++) {
        if (tabel_font_unikode[i].codepoint == codepoint) {
            return tabel_font_unikode[i].data;
        }
    }
    
    /* Kembalikan placeholder jika tidak ditemukan */
    return tabel_font_unikode[JUMLAH_ENTRI_FONT - 1].data;
}

/* Gambar satu karakter Unicode */
void unikode_gambar_karakter(InfoFramebuffer *fb, int x, int y,
    unsigned int codepoint, WarnaRGB latar, WarnaRGB belakang) {
    
    int i, j;
    const unsigned char *glyph;
    unsigned char *fb_ptr;
    int byte_per_piksel;
    int lokasi;
    unsigned char *piksel_ptr;
    int piksel_x, piksel_y;
    
    /* Ambil data glyph */
    glyph = unikode_data_font(codepoint);
    fb_ptr = (unsigned char *)fb->ptr;
    byte_per_piksel = fb->bpp / 8;
    
    /* Gambar karakter per piksel */
    for (i = 0; i < HURUF_TINGGI; i++) {
        for (j = 0; j < HURUF_LEBAR; j++) {
            piksel_x = x + j;
            piksel_y = y + i;
            
            /* Cek batas layar */
            if (piksel_x >= 0 && piksel_x < fb->lebar &&
                piksel_y >= 0 && piksel_y < fb->tinggi) {
                
                lokasi = (piksel_y * fb->panjang_baris) +
                         (piksel_x * byte_per_piksel);
                piksel_ptr = fb_ptr + lokasi;
                
                if (glyph[i] & (1 << (7 - j))) {
                    /* Piksel latar depan */
                    if (fb->bpp == 32) {
                        piksel_ptr[0] = latar.b;
                        piksel_ptr[1] = latar.g;
                        piksel_ptr[2] = latar.r;
                        piksel_ptr[3] = 0;
                    } else if (fb->bpp == 24) {
                        piksel_ptr[0] = latar.b;
                        piksel_ptr[1] = latar.g;
                        piksel_ptr[2] = latar.r;
                    } else if (fb->bpp == 16) {
                        /* Format RGB565 */
                        unsigned short warna;
                        warna = ((latar.r >> 3) << 11) |
                                ((latar.g >> 2) << 5) |
                                (latar.b >> 3);
                        *((unsigned short*)piksel_ptr) = warna;
                    }
                } else {
                    /* Piksel background */
                    if (fb->bpp == 32) {
                        piksel_ptr[0] = belakang.b;
                        piksel_ptr[1] = belakang.g;
                        piksel_ptr[2] = belakang.r;
                        piksel_ptr[3] = 0;
                    } else if (fb->bpp == 24) {
                        piksel_ptr[0] = belakang.b;
                        piksel_ptr[1] = belakang.g;
                        piksel_ptr[2] = belakang.r;
                    } else if (fb->bpp == 16) {
                        /* Format RGB565 */
                        unsigned short warna;
                        warna = ((belakang.r >> 3) << 11) |
                                ((belakang.g >> 2) << 5) |
                                (belakang.b >> 3);
                        *((unsigned short*)piksel_ptr) = warna;
                    }
                }
            }
        }
    }
}

/* Gambar string UTF-8 */
void unikode_gambar_string(InfoFramebuffer *fb, int x, int y,
    const char *str, WarnaRGB latar, WarnaRGB belakang) {
    
    int x_saat_ini = x;
    int y_saat_ini = y;
    const char *p = str;
    unsigned int codepoint;
    int panjang_karakter;
    
    if (!str) {
        return;
    }
    
    while (*p) {
        panjang_karakter = unikode_utf8_ke_codepoint(p, &codepoint);
        
        if (panjang_karakter == 0) {
            /* Karakter tidak valid, lewati */
            p++;
            continue;
        }
        
        if (codepoint == '\n') {
            /* Baris baru */
            y_saat_ini += HURUF_TINGGI;
            x_saat_ini = x;
        } else if (codepoint == '\r') {
            /* Carriage return */
            x_saat_ini = x;
        } else if (codepoint == '\t') {
            /* Tab - 4 karakter */
            x_saat_ini += HURUF_LEBAR * 4;
            if (x_saat_ini >= fb->lebar) {
                x_saat_ini = x;
                y_saat_ini += HURUF_TINGGI;
            }
        } else {
            /* Karakter normal */
            unikode_gambar_karakter(fb, x_saat_ini, y_saat_ini,
                codepoint, latar, belakang);
            x_saat_ini += HURUF_LEBAR;
            
            /* Wrap otomatis */
            if (x_saat_ini >= fb->lebar) {
                x_saat_ini = x;
                y_saat_ini += HURUF_TINGGI;
            }
        }
        
        p += panjang_karakter;
    }
}

/* Hitung lebar teks UTF-8 dalam piksel */
int unikode_lebar_teks(const char *str) {
    int lebar = 0;
    const char *p = str;
    unsigned int codepoint;
    int panjang_karakter;
    
    if (!str) {
        return 0;
    }
    
    while (*p) {
        panjang_karakter = unikode_utf8_ke_codepoint(p, &codepoint);
        
        if (panjang_karakter == 0) {
            p++;
            continue;
        }
        
        if (codepoint == '\n' || codepoint == '\r') {
            lebar = 0;
        } else if (codepoint == '\t') {
            lebar += HURUF_LEBAR * 4;
        } else {
            lebar += HURUF_LEBAR;
        }
        
        p += panjang_karakter;
    }
    
    return lebar;
}

/* Hitung tinggi teks UTF-8 dalam piksel */
int unikode_tinggi_teks(const char *str) {
    int tinggi = HURUF_TINGGI;
    int baris = 1;
    const char *p = str;
    unsigned int codepoint;
    int panjang_karakter;
    
    if (!str) {
        return HURUF_TINGGI;
    }
    
    while (*p) {
        panjang_karakter = unikode_utf8_ke_codepoint(p, &codepoint);
        
        if (panjang_karakter == 0) {
            p++;
            continue;
        }
        
        if (codepoint == '\n') {
            baris++;
        }
        
        p += panjang_karakter;
    }
    
    return tinggi * baris;
}
