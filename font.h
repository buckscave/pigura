/* Fungsi untuk menggambar satu karakter */
static void font_draw_char(FBInfo *fb, int x, int y, char ch, 
                         RGBColor fg, RGBColor bg) {
    int i, j;
    const unsigned char *glyph;
    unsigned char *fb_ptr = (unsigned char *)fb->ptr;
    int bytes_per_pixel = fb->bpp / 8;
    
    /* Pastikan karakter valid */
    if (ch < 32 || ch > 126) 
        ch = 32; /* gunakan spasi untuk karakter tidak dikenal */
    
    /* Ambil data glyph */
    glyph = &fontdata_8x16[(ch - 32) * FONT_HEIGHT];
    
    /* Gambar karakter */
    for (i = 0; i < FONT_HEIGHT; i++) {
        for (j = 0; j < FONT_WIDTH; j++) {
            int pixel_x = x + j;
            int pixel_y = y + i;
            
            /* Cek batas layar */
            if (pixel_x >= 0 && pixel_x < fb->width && 
                pixel_y >= 0 && pixel_y < fb->height) {
                int location = (pixel_y * fb->line_length) + (pixel_x * bytes_per_pixel);
                unsigned char *pixel_ptr = fb_ptr + location;
                
                if (glyph[i] & (1 << (7 - j))) {
                    /* Foreground */
                    if (fb->bpp == 32) {
                        pixel_ptr[0] = bg.b; /* Biru */
                        pixel_ptr[1] = bg.g; /* Hijau */
                        pixel_ptr[2] = bg.r; /* Merah */
                        pixel_ptr[3] = 0;    /* Alpha */
                    } else if (fb->bpp == 24) {
                        pixel_ptr[0] = bg.b;
                        pixel_ptr[1] = bg.g;
                        pixel_ptr[2] = bg.r;
                    } else if (fb->bpp == 16) {
                        /* RGB565 */
                        unsigned short color = ((bg.r >> 3) << 11) | 
                                              ((bg.g >> 2) << 5) | 
                                              (bg.b >> 3);
                        *((unsigned short*)pixel_ptr) = color;
                    }
                } else {
                    /* Background */
                    if (fb->bpp == 32) {
                        pixel_ptr[0] = fg.b;
                        pixel_ptr[1] = fg.g;
                        pixel_ptr[2] = fg.r;
                        pixel_ptr[3] = 0;
                    } else if (fb->bpp == 24) {
                        pixel_ptr[0] = fg.b;
                        pixel_ptr[1] = fg.g;
                        pixel_ptr[2] = fg.r;
                    } else if (fb->bpp == 16) {
                        unsigned short color = ((fg.r >> 3) << 11) | 
                                              ((fg.g >> 2) << 5) | 
                                              (fg.b >> 3);
                        *((unsigned short*)pixel_ptr) = color;
                    }
                }
            }
        }
    }
}

/* Fungsi untuk menggambar string */
static void font_draw_string(FBInfo *fb, int x, int y, const char *str, 
                           RGBColor fg, RGBColor bg) {
    int current_x = x;
    int current_y = y;
    
    while (*str) {
        if (*str == '\n') {
            current_y += FONT_HEIGHT;
            current_x = x;
        } else if (*str == '\r') {
            current_x = x;
        } else if (*str == '\t') {
            current_x += FONT_WIDTH * 4; /* Tab = 4 spasi */
            if (current_x >= fb->width) {
                current_x = x;
                current_y += FONT_HEIGHT;
            }
        } else {
            font_draw_char(fb, current_x, current_y, *str, fg, bg);
            current_x += FONT_WIDTH;
            
            /* Wrap otomatis jika melebihi lebar layar */
            if (current_x >= fb->width) {
                current_x = x;
                current_y += FONT_HEIGHT;
            }
        }
        str++;
    }
}

/* Fungsi untuk menghitung lebar teks dalam piksel */
static int font_text_width(const char *str) {
    int width = 0;
    
    while (*str) {
        if (*str == '\n' || *str == '\r') {
            /* Reset lebar untuk baris baru */
            width = 0;
        } else if (*str == '\t') {
            width += FONT_WIDTH * 4;
        } else {
            width += FONT_WIDTH;
        }
        str++;
    }
    
    return width;
}

/* Fungsi untuk menghitung tinggi teks dalam piksel */
static int font_text_height(const char *str) {
    int height = FONT_HEIGHT;
    int lines = 1;
    
    while (*str) {
        if (*str == '\n') {
            lines++;
        }
        str++;
    }
    
    return height * lines;
}

