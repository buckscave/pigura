/* Fungsi untuk mendapatkan data font dari kode poin Unicode */
static const unsigned char* unicode_get_font_data(unsigned int codepoint) {
    int i;
    
    for (i = 0; i < UNICODE_CHARS_SUPPORTED; i++) {
        if (unicode_font_table[i].codepoint == codepoint) {
            return unicode_font_table[i].data;
        }
    }
    
    /* Kembalikan data untuk karakter placeholder jika tidak ditemukan */
    return unicode_font_table[UNICODE_CHARS_SUPPORTED-1].data;
}

/* Fungsi untuk mendekode UTF-8 */
static int unicode_utf8_to_codepoint(const char *str, unsigned int *codepoint) {
    unsigned char c = (unsigned char)*str;
    
    if (c < 0x80) {
        /* ASCII (1 byte) */
        *codepoint = c;
        return 1;
    } else if ((c & 0xE0) == 0xC0) {
        /* 2 byte sequence */
        if ((str[1] & 0xC0) != 0x80) return 0;
        *codepoint = ((c & 0x1F) << 6) | (str[1] & 0x3F);
        return 2;
    } else if ((c & 0xF0) == 0xE0) {
        /* 3 byte sequence */
        if ((str[1] & 0xC0) != 0x80 || (str[2] & 0xC0) != 0x80) return 0;
        *codepoint = ((c & 0x0F) << 12) | ((str[1] & 0x3F) << 6) | (str[2] & 0x3F);
        return 3;
    } else if ((c & 0xF8) == 0xF0) {
        /* 4 byte sequence */
        if ((str[1] & 0xC0) != 0x80 || (str[2] & 0xC0) != 0x80 || (str[3] & 0xC0) != 0x80) return 0;
        *codepoint = ((c & 0x07) << 18) | ((str[1] & 0x3F) << 12) | ((str[2] & 0x3F) << 6) | (str[3] & 0x3F);
        return 4;
    }
    
    return 0; /* Invalid UTF-8 */
}

/* Fungsi untuk menggambar satu karakter Unicode */
static void unicode_draw_char(FBInfo *fb, int x, int y, unsigned int codepoint, 
                            RGBColor fg, RGBColor bg) {
    int i, j;
    const unsigned char *glyph;
    unsigned char *fb_ptr = (unsigned char *)fb->ptr;
    int bytes_per_pixel = fb->bpp / 8;
    
    /* Ambil data glyph */
    glyph = unicode_get_font_data(codepoint);
    
    /* Gambar karakter */
    for (i = 0; i < FONT_HEIGHT; i++) {
        for (j = 0; j < FONT_WIDTH; j++) {
            int pixel_x = x + j;
            int pixel_y = y + i;
            
            /* Cek batas layar */
            if (pixel_x >= 0 && pixel_x < fb->width && 
                pixel_y >= 0 && pixel_y < fb->height) {
                int location = (pixel_y * fb->line_length) + (pixel_x * bytes_per_pixel);
                unsigned char *pixel_ptr = fb_ptr + location;
                
                if (glyph[i] & (1 << (7 - j))) {
                    /* Foreground */
                    if (fb->bpp == 32) {
                        pixel_ptr[0] = fg.b; /* Biru */
                        pixel_ptr[1] = fg.g; /* Hijau */
                        pixel_ptr[2] = fg.r; /* Merah */
                        pixel_ptr[3] = 0;    /* Alpha */
                    } else if (fb->bpp == 24) {
                        pixel_ptr[0] = fg.b;
                        pixel_ptr[1] = fg.g;
                        pixel_ptr[2] = fg.r;
                    } else if (fb->bpp == 16) {
                        /* RGB565 */
                        unsigned short color = ((fg.r >> 3) << 11) | 
                                              ((fg.g >> 2) << 5) | 
                                              (fg.b >> 3);
                        *((unsigned short*)pixel_ptr) = color;
                    }
                } else {
                    /* Background */
                    if (fb->bpp == 32) {
                        pixel_ptr[0] = bg.b;
                        pixel_ptr[1] = bg.g;
                        pixel_ptr[2] = bg.r;
                        pixel_ptr[3] = 0;
                    } else if (fb->bpp == 24) {
                        pixel_ptr[0] = bg.b;
                        pixel_ptr[1] = bg.g;
                        pixel_ptr[2] = bg.r;
                    } else if (fb->bpp == 16) {
                        unsigned short color = ((bg.r >> 3) << 11) | 
                                              ((bg.g >> 2) << 5) | 
                                              (bg.b >> 3);
                        *((unsigned short*)pixel_ptr) = color;
                    }
                }
            }
        }
    }
}

/* Fungsi untuk menggambar string UTF-8 */
static void unicode_draw_string(FBInfo *fb, int x, int y, const char *str, 
                               RGBColor fg, RGBColor bg) {
    int current_x = x;
    int current_y = y;
    const char *p = str;
    
    while (*p) {
        unsigned int codepoint;
        int char_len = unicode_utf8_to_codepoint(p, &codepoint);
        
        if (char_len == 0) {
            /* Karakter tidak valid, lewati */
            p++;
            continue;
        }
        
        if (codepoint == '\n') {
            current_y += FONT_HEIGHT;
            current_x = x;
        } else if (codepoint == '\r') {
            current_x = x;
        } else if (codepoint == '\t') {
            current_x += FONT_WIDTH * 4; /* Tab = 4 spasi */
            if (current_x >= fb->width) {
                current_x = x;
                current_y += FONT_HEIGHT;
            }
        } else {
            unicode_draw_char(fb, current_x, current_y, codepoint, fg, bg);
            current_x += FONT_WIDTH;
            
            /* Wrap otomatis jika melebihi lebar layar */
            if (current_x >= fb->width) {
                current_x = x;
                current_y += FONT_HEIGHT;
            }
        }
        
        p += char_len;
    }
}

/* Fungsi untuk menghitung lebar teks UTF-8 dalam piksel */
static int unicode_text_width(const char *str) {
    int width = 0;
    const char *p = str;
    
    while (*p) {
        unsigned int codepoint;
        int char_len = unicode_utf8_to_codepoint(p, &codepoint);
        
        if (char_len == 0) {
            p++;
            continue;
        }
        
        if (codepoint == '\n' || codepoint == '\r') {
            /* Reset lebar untuk baris baru */
            width = 0;
        } else if (codepoint == '\t') {
            width += FONT_WIDTH * 4;
        } else {
            width += FONT_WIDTH;
        }
        
        p += char_len;
    }
    
    return width;
}

/* Fungsi untuk menghitung tinggi teks UTF-8 dalam piksel */
static int unicode_text_height(const char *str) {
    int height = FONT_HEIGHT;
    int lines = 1;
    const char *p = str;
    
    while (*p) {
        unsigned int codepoint;
        int char_len = unicode_utf8_to_codepoint(p, &codepoint);
        
        if (char_len == 0) {
            p++;
            continue;
        }
        
        if (codepoint == '\n') {
            lines++;
        }
        
        p += char_len;
    }
    
    return height * lines;
}

